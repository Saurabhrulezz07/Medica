                                    <h4>2019-05-09 17:46:21</h4>
                                </a>
                                <p>Scientists at the University of Southern California in Los Angeles recently tested
                                    what they describe as the "fasting-mimicking" diet on a mouse model of inflammatory
                                    bowel disease (IBD). In a study paper that now features in the journal Cell Reports,
                                    they describe how, compared with water-only fasting, periodic 4-day cycles of the
                                    fasting-mimicking diet "partially reversed" hallmarks of IBD in the mice. They saw
                                    that the diet reduced inflammation and increased populations of stem cells in the
                                    mice's intestines. Stem cells are essential for tissue repair and regeneration. In
                                    addition, the team observed that these effects appeared, in part, to be due to an
                                    increase in beneficial gut bacteria. Results from humans also showed that the diet
                                    reduced markers of inflammation and associated immune cells. Taking these results
                                    together, the researchers conclude that a low-calorie, plant-based,
                                    fasting-mimicking diet has potential as an effective treatment for IBD.
                                    Corresponding study author Valter Longo, a professor of biological sciences, says
                                    that their investigation is the first IBD study to bring together "two worlds of
                                    research." "The first [world]," he explains, "is about what you should eat every
                                    day, and many studies point to a diet rich in vegetables, nuts, and olive oil. The
                                    second is fasting and its effects on inflammation, regeneration, and aging." He and
                                    his colleagues suggest that the reason that water-based fasting does not seem to be
                                    as effective as the fasting-mimicking diet could be because while fasting produces
                                    many of the desired effects, the body still needs essential nutrients to do the
                                    rest.</p>
